// Errors

// There are three main types of errors in JavaScript: syntax errors
// , runtime
//  errors, and logical errors
// .
// 1. Syntax errors:
// These errors occur when the code is not written correctly according to the rules of the JavaScript language. For example, a missing semicolon or a misspelled keyword can cause a syntax error.

// Syntax error
// var x = 1 + 2;

// 2. Runtime errors:
// These errors occur when the code is running and something unexpected happens. For example, trying to access a variable that does not exist or trying to divide by zero can cause a runtime error.

// Runtime error
// var y = undefined;
// console.log(y.length);

// 3. Logical errors:
// These errors occur when the code is written correctly but does not produce the intended results. For example, a mistake in a mathematical calculation or a typo in a conditional statement can cause a logical error.

// Logical error
// var z = 10;
// if (z > 5) {
//   console.log("z is greater than 5");
// } else {
//   console.log("z is not greater than 5");
// }

